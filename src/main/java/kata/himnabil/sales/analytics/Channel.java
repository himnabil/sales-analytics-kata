package kata.himnabil.sales.analytics;

/**
 * Represents the different sales channels through which orders can be placed.
 * <p>
 * This enum defines the possible channels that can be used for sales transactions
 * in the system.
 */
public enum Channel {
    /**
     * Represents orders placed through the web platform.
     */
    WEB,

    /**
     * Represents orders placed through the mobile application.
     */
    MOBILE,

    /**
     * Represents orders placed in physical stores.
     */
    STORE
}
